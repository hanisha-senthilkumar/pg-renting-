function renderFeaturedPGs() {
    const grid = document.getElementById('featured-grid');
    if (!grid) return;

    const featured = allPGData.filter(pg => [1, 2, 3, 6].includes(pg.id));
    grid.innerHTML = featured.map(pg => createPGCard(pg)).join('');
}

function renderListings(data) {
    const grid = document.getElementById('pg-grid');
    if (!grid) return;

    if (data.length === 0) {
        grid.innerHTML = '<div style="grid-column: 1/-1; text-align: center; padding: 40px; color: var(--text-muted);">No PGs found matching your criteria.</div>';
        return;
    }

    grid.innerHTML = data.map(pg => createPGCard(pg)).join('');
}

function setupHomeSearch() {
    const searchBtn = document.getElementById('home-search-btn');
    if (searchBtn) {
        searchBtn.addEventListener('click', () => {
            const location = document.getElementById('home-location').value;
            const gender = document.getElementById('home-gender').value;
            window.location.href = `listings.html?loc=${location}&gender=${gender}`;
        });
    }
}

function setupSearch() {
    const searchInput = document.getElementById('search-input');
    const sortSelect = document.getElementById('sort-select');

    if (searchInput) {
        searchInput.addEventListener('input', applyFilters);
    }
    if (sortSelect) {
        sortSelect.addEventListener('change', applyFilters);
    }

    // Check URL params for initial filters
    const params = new URLSearchParams(window.location.search);
    const gender = params.get('gender');
    if (gender) {
        const checkbox = document.querySelector(`input[name="gender"][value="${gender}"]`);
        if (checkbox) checkbox.checked = true;
    }
    applyFilters();
}

function setupFilters() {
    const filters = document.querySelectorAll('.filters-sidebar input');

    if (filters.length === 0) return;

    filters.forEach(filter => {
        filter.addEventListener('change', () => {
            applyFilters();
        });
    });
}

function applyFilters() {
    let filteredData = [...allPGData];

    // Text search
    const searchInput = document.getElementById('search-input');
    if (searchInput && searchInput.value) {
        const query = searchInput.value.toLowerCase();
        filteredData = filteredData.filter(pg =>
            pg.name.toLowerCase().includes(query) ||
            pg.location.toLowerCase().includes(query)
        );
    }

    // Get selected cities
    const cities = Array.from(document.querySelectorAll('input[name="city"]:checked')).map(cb => cb.value);
    if (cities.length > 0) {
        filteredData = filteredData.filter(pg => cities.includes(pg.city));
    }

    // Get selected genders
    const genders = Array.from(document.querySelectorAll('input[name="gender"]:checked')).map(cb => cb.value);
    if (genders.length > 0) {
        filteredData = filteredData.filter(pg => genders.includes(pg.gender));
    }

    // Get selected amenities
    const amenities = Array.from(document.querySelectorAll('input[name="amenities"]:checked')).map(cb => cb.value);
    if (amenities.length > 0) {
        amenities.forEach(amenity => {
            filteredData = filteredData.filter(pg => pg[amenity] === true);
        });
    }

    // Sorting
    const sortValue = document.getElementById('sort-select')?.value || 'popularity';
    if (sortValue === 'price-low') {
        filteredData.sort((a, b) => parseInt(a.price) - parseInt(b.price));
    } else if (sortValue === 'price-high') {
        filteredData.sort((a, b) => parseInt(b.price) - parseInt(a.price));
    } else if (sortValue === 'rating') {
        filteredData.sort((a, b) => b.rating - a.rating);
    }

    renderListings(filteredData);
}

function setupForm() {
    const form = document.getElementById('add-pg-form');
    if (form) {
        form.addEventListener('submit', async (e) => {
            e.preventDefault();
            const btn = form.querySelector('button[type="submit"]');
            const originalText = btn.textContent;

            btn.textContent = 'Submitting...';
            btn.disabled = true;

            // Gather data
            const newPg = {
                id: Date.now(),
                name: document.getElementById('pgName').value,
                city: document.getElementById('city').value,
                location: document.getElementById('area').value + ', ' + document.getElementById('city').value,
                address: document.getElementById('address').value,
                type: document.getElementById('pgType').value,
                price: document.getElementById('rent').value,
                phone: document.getElementById('phone').value,
                gender: document.getElementById('pgType').value === 'Boys' ? 'Male' : 'Female',
                rating: 0,
                reviews: 0,
                food: false, // Defaulting false, could read from checkboxes
                ac: false,
                wifi: false,
                description: "Newly added PG listing.",
                images: ["https://images.unsplash.com/photo-1522708323590-d24dbb6b0267?auto=format&fit=crop&q=80&w=800"], // placeholder
                mapUrl: ""
            };

            try {
                // Save to localStorage first
                localPGs.push(newPg);
                localStorage.setItem('customPGs', JSON.stringify(localPGs));

                // Call actual backend API
                const res = await fetch('/api/pg', {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify(newPg)
                });

                if (res.ok) {
                    alert('PG Listing submitted successfully! It will be live after review.');
                    form.reset();
                } else {
                    alert('Failed to submit listing. Please try again.');
                }
            } catch (err) {
                // Fallback for Demo without server
                alert('Demo Listing submitted successfully! It is now saved in your browser.');
                form.reset();
            } finally {
                btn.textContent = originalText;
                btn.disabled = false;
            }
        });
    }
}

