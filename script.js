document.addEventListener('DOMContentLoaded', () => {
    // Mobile Menu Toggle
    const menuBtn = document.querySelector('.menu-toggle');
    const navLinks = document.querySelector('.nav-links');
    if (menuBtn && navLinks) {
        menuBtn.addEventListener('click', () => {
            navLinks.classList.toggle('active');
        });
    }

    // Determine current page
    const currentPage = window.location.pathname.split('/').pop();

    if (currentPage === 'listings.html') {
        renderListings(allPGData);
        setupFilters();
        setupSearch();
    } else if (currentPage === '' || currentPage === 'index.html') {
        renderFeaturedPGs();
        setupHomeSearch();
    } else if (currentPage === 'add-pg.html') {
        setupForm();
    } else if (currentPage === 'details.html' || window.location.pathname.includes('details.html')) {
        renderDetails();
    }
});

function toggleWishlist(e, id) {
    e.preventDefault();
    e.stopPropagation();
    let wishlist = JSON.parse(localStorage.getItem('pgWishlist') || '[]');
    if (wishlist.includes(id)) {
        wishlist = wishlist.filter(item => item !== id);
    } else {
        wishlist.push(id);
    }
    localStorage.setItem('pgWishlist', JSON.stringify(wishlist));
    // re-render current view if on listings or home
    if (window.location.pathname.includes('listings.html')) {
        applyFilters();
    } else {
        renderFeaturedPGs();
    }
}

